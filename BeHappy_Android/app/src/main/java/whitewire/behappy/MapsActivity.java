    public LatLng cityToPos(String city) {
        if (Geocoder.isPresent()) {
            try {
                Geocoder gc = new Geocoder(this);

                // Getting possible list of addresses from geocoder up to a maximum of five
                List<Address> addresses = gc.getFromLocationName(city, 5);

                if (addresses == null) {
                    return null;
                }

                // Getting most relevant address as a location
                Address location = addresses.get(0);
                location.getLatitude();
                location.getLongitude();

                // Creating LatLng location based on retrieved latitude and longitude
                LatLng p1 = new LatLng(location.getLatitude(), location.getLongitude());
                return p1;
            } catch (IOException e) {
                Toast.makeText(this, "IOException error is: " + e.toString(), Toast.LENGTH_SHORT).show();
            }
        }
        return null;
    }

